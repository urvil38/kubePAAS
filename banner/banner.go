func PrintProjectInfo(config latest.KubepaasConfig, projectMetadata config.ProjectMetaData) string {
	wd, _ := os.Getwd()
	return color.HiYellowString(
		projectInfo,
		formatText(config.Metadata.Name),
		formatText(projectMetadata.CurrentVersion),
		formatText(config.Deploy.Runtime),
		formatText(wd),
		formatText("https://"+config.Metadata.Name+"."+projectMetadata.Domain),
		formatText(time.Now().Format("2006-01-02 3:4:5 PM")))
}

